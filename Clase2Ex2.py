#Promedio de ingresos

mes1 = input("Ingrese el valor del primer mes: ")
mes2 = input("Ingrese el valor del segundo mes: ")
mes3 = input("Ingrese el valor del tercer mes: ")
mes4 = input("Ingrese el valor del cuarto mes: ")
mes5 = input("Ingrese el valor del quinto mes: ")
mes6 = input("Ingrese el valor del sexto mes: ")

prom = (int(mes1) + int(mes2) + int(mes3) + int(mes4) + int(mes5) + int(mes6)) / 6

print(f"El ingreso promedio en el semestre es de {prom}")